#include "libft.h"

int	ft_delchr(const char *s, char c)
{
	size_t i;

	i = 0;
	while (*(s + i))
	{
		if (*(s + i) == c)
			return ((int)i);
		i++;
	}
	if (*(s + i) == c && c == '\0')
		return (i);
	return (-1);
}
